package com.store.storage;

public class Student {
	String stId;
	String stName;
	
	public Student(String stId, String stName) {
		super();
		this.stId = stId;
		this.stName = stName;
	}
	public String getStId() {
		return stId;
	}
	public void setStId(String stId) {
		this.stId = stId;
	}
	public String getStName() {
		return stName;
	}
	public void setStName(String stName) {
		this.stName = stName;
	}
	
	

}
